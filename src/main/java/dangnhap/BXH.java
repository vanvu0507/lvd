    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
                DefaultTableModel model = (DefaultTableModel) tblBXH.getModel();
        if (tblBXH.getSelectedRow()==-1) {
            if (tblBXH.getRowCount()==0) {
                lblError.setText("Trống");
            }
            else{
                lblError.setText("Bạn cần chọn tên Đội bóng");
            }
        }
        else{
            model.removeRow(tblBXH.getSelectedRow());
        }
        
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
                if (tblBXH.getSelectedRow()==-1) {
            if (tblBXH.getRowCount()==0) {
                lblError.setText("Trống");
            }
            else{
                lblError.setText("Bạn cần Phải điền đầy đủ thông tin");
            }
        }
        else{
            DefaultTableModel model = (DefaultTableModel) tblBXH.getModel();
            model.setValueAt(txtStt.getText(), tblBXH.getSelectedRow(), 0);
            model.setValueAt(txtDoibong.getText(),tblBXH.getSelectedRow(), 1);
            model.setValueAt(txtSotran.getText(), tblBXH.getSelectedRow(), 2);
            model.setValueAt(txtThang.getText(), tblBXH.getSelectedRow(), 3);
            model.setValueAt(txtThua.getText(), tblBXH.getSelectedRow(), 4);
            model.setValueAt(txtHoa.getText(), tblBXH.getSelectedRow(), 5);
            model.setValueAt(txtBanthang.getText(), tblBXH.getSelectedRow(), 6);
            model.setValueAt(txtBanthua.getText(), tblBXH.getSelectedRow(), 7);
            model.setValueAt(txtDiem.getText(), tblBXH.getSelectedRow(), 8);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

