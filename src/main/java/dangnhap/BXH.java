    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        lblError.setText("");
        DefaultTableModel model =(DefaultTableModel) tblBXH.getModel();
        if (!txtDoibong.getText().trim().equals("")) {
            model.addRow(new Object[]{txtStt.getText(),txtDoibong.getText(), 
            txtSotran.getText(),txtThang.getText(),txtThua.getText(),txtHoa.getText(),
            txtBanthang.getText(),txtBanthua.getText(),txtDiem.getText()});
        } else {
            lblError.setText("Không được bỏ trống các trường");
        }
    }//GEN-LAST:event_btnThemActionPerformed

